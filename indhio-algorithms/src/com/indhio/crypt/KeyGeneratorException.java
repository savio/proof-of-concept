package com.indhio.crypt;

public class KeyGeneratorException extends Exception {
	private static final long serialVersionUID = 3258134639422748468L;

	public KeyGeneratorException() {
		super();
	}

	public KeyGeneratorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public KeyGeneratorException(String arg0) {
		super(arg0);
	}

	public KeyGeneratorException(Throwable arg0) {
		super(arg0);
	}

}

