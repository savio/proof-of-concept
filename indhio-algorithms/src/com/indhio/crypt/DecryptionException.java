package com.indhio.crypt;

public class DecryptionException extends Exception {
	private static final long serialVersionUID = 3257844368319854644L;

	public DecryptionException() {
		super();
	}

	public DecryptionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DecryptionException(String arg0) {
		super(arg0);
	}

	public DecryptionException(Throwable arg0) {
		super(arg0);
	}
}

